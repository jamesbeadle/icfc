    <p>
        As our ecosystem grows and new features are introduced across our range of applications we will always look to utilise the ICFC token where possible to ensure any profits are ultimately returned to token holders.
    </p>
    
    <p class="text-xl cta-text">
        ICFC’s Mission: Redefining Football Governance
    </p>

    <p>
        Traditional football clubs are commonly governed by a small group of owners or directors, often sidelining the club’s real heartbeat - its supporters. 
        ICFC’s mission is to democratise football club ownership by enabling fans, players and community members to directly influence operational decisions. 
    </p>

    <p>Our Key Goals:</p>

    <ul>
        <li>
            Ensure Club Success: Prove that decentralising the key decisions with fans will ultimately lead to club success through climbing the football pyramid.
        </li>
        <li>
            Boost ICP Adoption & Value: Showcase a key ICP blockchain feature (SNS Governance) to drive large-scale user adoption, increasing ecosystem demand.
        </li>
        <li>
            Enhance Fan Engagement: Reward supporters for physically attending matches, strengthening loyalty and participation.
        </li>
        <li>
            Build a Football Reality Movement: Inspired by the Wrexham model, ICFC will produce content blending real-world football and blockchain technology to reach mainstream audiences. 
        </li>
        <li>
            Promote Transparency: On-chain governance eliminates opaque boardroom decisions. 
        </li>
        <li>
            Unleash Global Potential: Allowing anyone worldwide to own tokens in a club, expanding the fan base and investment scope. 
        </li>
    </ul>
    
