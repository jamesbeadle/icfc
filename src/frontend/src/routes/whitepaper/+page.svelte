    <p>
        The OpenFPL DAO was initially developed using the FPL token for our fantasy football platform. 
        However, it has rapidly expanded into a broader ecosystem encompassing women’s fantasy football, community-driven governance, and a unique betting platform.
    </p>
    
    <p>
        We believe the the next step on the road to decentralising football ownership for fans is rebrand our SNS from OpenFPL to ICFC (Internet Computer Football Club) and build a football club for the world. 
    </p>
    
    <p>
        By using blockchain-based governance, ICFC empowers fans and stakeholders to participate in club decisions, from hiring managers to scouting players through a tokenised voting system, running 100% on a decentralised blockchain.
    </p>
    
    <p>
        This whitepaper outlines the overarching vision, technological infrastructure, token economics, and long-term strategy for expanding our decentralised football infrastructure through <a href="https://icfc.app" target="_blank">icfc.app</a>.
    </p>


    <p class="text-xl cta-text">
        A Purposeful Rebrand
    </p>

    <p>
        ICFC will act as the parent brand for our existing valuable football brands, providing a unified and consistent focal point for promotion. 
    </p>

    <p>
        The original OpenFPL name is closely associated with “Fantasy Premier League”, yet our project has evolved far beyond fantasy football, creating a governance backed dataset powering multiple football based applications. 
        This rebrand reflects our expanded focus on true football club governance, ensuring supporters have a stake in decision-making.
    </p>

